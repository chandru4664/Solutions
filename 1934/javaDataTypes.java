package com.htc.javatraining.exercises;

public class javaDataTypes {
	
	public static void main(String[] args) {
		
		int i = 1000;
		float f = 25.65f;
		double d = 78.738363;
		char c = 'c';
		boolean b = true;
		long l = 93738488;
		short s = 150;
		byte by = 1;
		
		System.out.println("Integer type value: " + i);
		System.out.println("Float type value: " + f);
		System.out.println("Double type value: " + d);
		System.out.println("Character type value: " + c);
		System.out.println("Boolean type value: " + b);
		System.out.println("Long type value: " + l);
		System.out.println("Short type value: " + s);
		System.out.println("Byte type value: " + by);
		
	}

}
