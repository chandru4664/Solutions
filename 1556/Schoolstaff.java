package oops.inheritance;

public class Schoolstaff {
	int employeeID;
	int salary;
	String name;
	int experience;
	Schoolstaff(int e,int s,String n,int j){
		employeeID=e;
		salary=s;
		name=n;
		experience=j;
		this.employeeID=e;
		this.salary=s;
		this.name=n;
		this.experience=j;
	}

}
